// Массив для хранения истории действий
let actionHistory = [];

function addContent(blockName) {
    const previewBlock = document.getElementById('preview');
    if (contentTemplates[blockName]) {
        // Сохраняем текущее состояние перед добавлением нового контента
        actionHistory.push(previewBlock.innerHTML);
        previewBlock.innerHTML += contentTemplates[blockName];
    }
}

function clearPreview() {
    const previewBlock = document.getElementById('preview');
    // Сохраняем текущее состояние перед очисткой
    actionHistory.push(previewBlock.innerHTML);
    previewBlock.innerHTML = '';
}

function undoLastAction() {
    if (actionHistory.length > 0) {
        const previewBlock = document.getElementById('preview');
        // Восстанавливаем предыдущее состояние
        previewBlock.innerHTML = actionHistory.pop();
    }
}

function copyToClipboard() {
    const previewBlock = document.getElementById('preview');
    const range = document.createRange();
    range.selectNodeContents(previewBlock); // Копируем только содержимое
    window.getSelection().removeAllRanges();
    window.getSelection().addRange(range);
    document.execCommand("copy");
    window.getSelection().removeAllRanges();
    alert("Скопировано в буфер обмена!");
}

function showPanel(panelName) {
    // Скрываем все панели
    document.getElementById('diadok-panel').style.display = 'none';
    document.getElementById('marking-panel').style.display = 'none';
    document.getElementById('extern-panel').style.display = 'none';

    // Показываем выбранную панель
    document.getElementById(panelName + '-panel').style.display = 'block';

    // Убираем класс active со всех кнопок и логотипов
    document.querySelectorAll('.top-controls button, .top-controls .logo').forEach(el => {
        el.classList.remove('active');
    });

    // Добавляем класс active к выбранной кнопке или логотипу
    const activeElement = document.querySelector(`.top-controls [onclick="showPanel('${panelName}')"]`);
    if (activeElement) {
        activeElement.classList.add('active');
    }
}